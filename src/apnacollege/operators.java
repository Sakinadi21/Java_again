package apnacollege;

public class operators {
    public static void main(String[] args) {
        //operators
        double a=12;
        double b=8;
        double sum=a+b;
        System.out.println(sum);//12+8=20
        double minus=a-b;
        System.out.println(minus);//12-8=4
        double mul=a*b;
        System.out.println(mul);//12*8=96
        double div=a/b;
        System.out.println(div);//12/8=1.5
        double module=a%b;
        System.out.println(module);//remainder 4
    }
}
